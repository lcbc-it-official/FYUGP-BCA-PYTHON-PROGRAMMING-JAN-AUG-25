# 5. Assignment Operators
# These operators assign values to variables and can combine assignment with arithmetic.
c = 7

print("\nAssignment Operators:")
c += 3  # Equivalent to c = c + 3
print("c += 3:", c)
c -= 2  # Equivalent to c = c - 2
print("c -= 2:", c)
c *= 2  # Equivalent to c = c * 2
print("c *= 2:", c)
c /= 3  # Equivalent to c = c / 3
print("c /= 3:", c)
c %= 2  # Equivalent to c = c % 2
print("c %= 2:", c)
